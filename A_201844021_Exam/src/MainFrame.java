    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String strSQL = "insert into memberinfo values (";
	strSQL += "'" + tbxNo.getText() + "', ";
	strSQL += "'" + tbxName.getText() + "', ";
	strSQL += "'" + tbxCharge.getText() + "', ";
	strSQL += "'" + tbxPhone.getText() + "', ";
	
	int tbxChargeValue = Integer.parseInt(tbxCharge.getText());
	
	String grade = "";
	
	if(tbxChargeValue >= 100000){
	    grade = "VIP";
	} else if(tbxChargeValue >= 50000){
	    grade = "Gold";
	} else if(tbxChargeValue < 50000){
	    grade = "Silver";
	}
	
	strSQL += "'" + grade + "')";
	
	try {
	    DBM.dbOpen();
	    
		DBM.DB_stmt.executeUpdate(strSQL);
		getDBData(getSortListSQL);
		
	    DBM.dbClose();
	} catch(Exception e){
	    System.out.println(e.getMessage());
	}
    }//GEN-LAST:event_btnInsertActionPerformed

