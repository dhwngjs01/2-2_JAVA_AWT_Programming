    public void resetTable(){
	int iCntRow = jTable1.getRowCount();
	
	for(int i = 0; i < iCntRow; i++){
	    if(jTable1.getValueAt(i, 0) != null){
		for(int j = 0; j < 5; j++){
		    jTable1.setValueAt(null, i, j);
		}
	    } else {
		break;
	    }
	}
    }
    
    public void getDBData(String strQuery){
	resetTable();

	int iCntRow = jTable1.getRowCount();
	
	try {
	    DBM.DB_rs = DBM.DB_stmt.executeQuery(strQuery);
	    
	    while(DBM.DB_rs.next()){
		for(int i = 0; i < jTable1.getRowCount(); i++){
		    if(jTable1.getValueAt(i, 0) == null){
			iCntRow = i;
			break;
		    }
		}
		
		String[] val = {
		    DBM.DB_rs.getString("NO"), 
		    DBM.DB_rs.getString("NAME"), 
		    DBM.DB_rs.getString("CHARGE"), 
		    DBM.DB_rs.getString("PHONE"), 
		    DBM.DB_rs.getString("GRADE")
		};
		
		for(int i = 0; i < 5; i++){
		    jTable1.setValueAt(val[i], iCntRow, i);
		}
	    }
	    
	    DBM.DB_rs.close();
	} catch(Exception e){
	    System.out.println(e);
	}
    }
    
    public String makeSQLWhere(String strQuery){
	switch(cbxSearch.getSelectedIndex()){
	    case 0 :
		strQuery += " where no = '" + tbxNo.getText() + "'";
		break;
	    case 1 :
		strQuery += " where name = '" + tbxName.getText() + "'";
		break;
	    case 2:
		strQuery += " where charge = '" + tbxCharge.getText() + "'";
		break;
	    case 3:
		strQuery += " where phone = '" + tbxPhone.getText() + "'";
		break;
	    default :
		strQuery = getSortListSQL;
		break;
	}
	
	return strQuery;
    }

